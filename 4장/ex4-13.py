# 아이디가 등록된 아이디인지 확인하는 프로그램
# 등록된 아이디는 리스트 형태로 저장되어 있고 등록된 아이디인 경우
# 패스워드를 묻는다. 단, 패스워드는 "1234"로 고정

user_list = ["홍길동", "김연아", "손연재", "손흥민", "아이유"]
password = "1234"

id = input("아이디를 입력하시오: ")
if id in user_list:
    pw = input("패스워드를 입력하시오: ")
    if pw == password:
        print("%s님이 로그인 하셨습니다." % id)
    else:
        print("패스워드가 틀렸습니다.")
else:
    print("회원가입이 되지 않았습니다.")